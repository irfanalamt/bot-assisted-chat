import mongoose from 'mongoose';

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
  },
  role: {
    type: String,
    enum: ['sysAdmin', 'clientAdmin', 'admin', 'agent'],
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
  },
  clientId: {
    type: String,
    required: true,
  },
});

export default mongoose.models.User || mongoose.model('User', userSchema);
